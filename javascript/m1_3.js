// 연산자
var a = 1, b = 2;

console.log(a+b);
console.log(a++);
console.log(b++);
// 증감, 감소 연산자
console.log(a);
console.log(b);

console.log(++a);
console.log(++b);
console.log(a);
console.log(b);

console.log("Math.pow(2,3): "+Math.pow(2,3));
